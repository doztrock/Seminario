/*
    
    CONSULTAR LAS ASIGNATURAS DEFINIDAS PARA UN GRADO, 
    DE ESTAS DESPLEGAR:
    
    -CODIGO
    -NOMBRE
    -CODIGO GRADO 
    -HORAS SEMANA

*/
SELECT 
    A.COASIG,
    A.NOMBREASIG,
    G.COGRA,
    A.HORASEM
FROM
    ASIGNATURA A
INNER JOIN 
    GRADO G
  ON    
    A.COGRA = G.COGRA;